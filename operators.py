num1 = 4
num2 = 2.5

print(f'{num1} + {num2} = {num1+num2}')
print(f'{num1} - {num2} = {num1-num2}')
print(f'{num1} * {num2} = {num1*num2}')
print(f'{num1} / {num2} = {num1/num2}') # return float
print(f'{num1} // {num2} = {num1//num2}') # return int
print(f'{num1} ** {num2} = {num1**num2}') # exponential 4^2.5

